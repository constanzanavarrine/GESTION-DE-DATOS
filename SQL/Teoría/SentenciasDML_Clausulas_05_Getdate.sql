Ejemplo de uso de GETDATE() en una consulta SELECT

SELECT GETDATE() AS FechaActual;
  -> Devuelve la fecha y hora actual del sistema.

  -> Útil para registrar cuándo se realizó una consulta o para comparar fechas en una base de datos.

  -> El resultado es una columna con el alias "FechaActual" que muestra la fecha y hora actuales.


SELECT GETDATE() AS HOY

-- HOY -> Alias para la columna que muestra la fecha y hora actual 

